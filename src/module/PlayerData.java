package module;



public class PlayerData {
	private String PlayerName;
	private int Kills;
	private int Deaths;
	private int Assists;
	private int CS;
	private int MatchKills;
	private int MatchDeaths;
	private int MatchAssists;
	private int MatchCS;
	
	
	
//	getter and setter for all player properties
	
	
	public void setPlayerName(String PlayerName) {
		this.PlayerName = PlayerName;
	}
	
	public String getPlayerName() {
		return PlayerName;
	}
	
	public void setKills(int Kills) {
		this.Kills = Kills;
	}
	
	public int getKills() {
		return Kills;
	}
	
	public void setDeaths(int Deaths) {
		this.Deaths = Deaths;
	}
	
	public int getDeaths() {
		return Deaths;
	}
	
	public void setAssists(int Assists) {
		this.Assists = Assists;
	}
	
	public int getAssists() {
		return Assists;
	}
	
	public void setCS(int CS) {
		this.CS = CS;
	}
	
	public int getCS() {
		return CS;
	}
	
	public void setMatchKills(int MatchKills) {
		this.MatchKills = MatchKills;
	}
	
	public int getMatchKills() {
		return MatchKills;
	}
	
	public void setMatchDeaths(int MatchDeaths) {
		this.MatchDeaths = MatchDeaths;
	}
	
	public int getMatchDeaths() {
		return MatchDeaths;
	}
	
	public void setMatchAssists(int MatchAssists) {
		this.MatchAssists = MatchAssists;
	}
	
	public int getMatchAssists() {
		return MatchAssists;
	}
	
	public void setMatchCS(int MatchCS) {
		this.MatchCS = MatchCS;
	}
	
	public int getMatchCS() {
		return MatchCS;
	}
	
	
}

